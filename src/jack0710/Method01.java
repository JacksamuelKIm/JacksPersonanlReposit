package jack0710;

public class Method01 {
	
	public static void show() {
		System.out.println("I am 'show method'.");
		} //매개변수와 반환값이 모두 없는 경우! 
	
	public static void main(String[] args) {
		System.out.println("Before 'show call'");
		show();
		System.out.println("After 'show call");
		
	} //main end

} //class end
